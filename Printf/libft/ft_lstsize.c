#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*t;

	if (!lst)
		return (0); 
	i = 1;
	t = lst;
	while (t->next)
	{
		t = t->next;
		i++;
	}
	return (i);
}
